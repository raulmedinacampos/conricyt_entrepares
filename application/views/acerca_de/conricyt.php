  <p>El acceso a la información  científica especializada, es un factor que contribuye a la generación de nuevo  conocimiento y al desarrollo de la investigación en México, por lo que en  septiembre de 2010, el Consejo Nacional de Ciencia y Tecnología, la Secretaría  de Educación Pública, la Asociación Nacional de Universidades e Instituciones  de Educación Superior, la Universidad Nacional Autónoma de México, el Instituto  Politécnico Nacional, el Centro de Investigación y de Estudios Avanzados del  IPN, la Universidad Autónoma Metropolitana, la Universidad de Guadalajara y la  Corporación Universitaria para el Desarrollo de Internet, firmaron un Convenio  de Colaboración para constituir formalmente el Consorcio Nacional de Recursos  de Información Científica y Tecnológica (CONRICYT).</p>
  <h3>Objetivo</h3>
  <p>Fortalecer las  capacidades de las Instituciones de Educación Superior y Centros de  Investigación para que el conocimiento científico y tecnológico universal sea  del dominio de los estudiantes, académicos, investigadores y otros usuarios,  ampliando, consolidando y facilitando el acceso a la información científica y  tecnológica en formatos digitales.</p>
