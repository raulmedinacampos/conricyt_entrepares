  <p><em>Entre Pares</em>, Seminario para publicar y navegar en las redes de la investigación científica, es parte de la  estrategia del Consorcio Nacional de Recursos de Información Científica y Tecnológica (CONRICYT), por promover la producción de artículos científicos  entre la comunidad académica nacional y su difusión a nivel internacional. El Seminario plantea un espacio de encuentro entre autores-investigadores  nacionales y las principales editoriales científicas internacionales.</p>

  <p>El Seminario está  dirigido a la comunidad científica nacional: estudiantes de posgrado, profesores,  investigadores, bibliotecarios, referencistas y a la sociedad en general.</p>

  <p>Las metas del Seminario son:</p>

  <blockquote>

    <p>1. Difundir la labor del  CONRICYT como una iniciativa interinstitucional, que a tres años de su creación  ha logrado un incremento positivo en el acceso a la información científica y  tecnológica, por parte de las Instituciones de Educación Superior y Centros de  Investigación del país, con el fin de coadyuvar en la producción de artículos  científicos mexicanos.</p>

    <p>2. Impulsar a las  revistas científicas mexicanas de alta calidad para que incrementen su  visibilidad a nivel internacional.</p>

  </blockquote>

