</p>

<h2>Las herramientas que ofrece Nature.</h2>
<p>
Dentro de la plataforma de nature.com se puede ubicar de manera muy simple la forma de recuperar la información, se puede hacer una búsqueda sencilla con una palabra clave o bien una búsqueda avanzada, nature.com tiene integrado además valiosas herramientas para investigadores que les ayudaran a hacer más fácil su trabajo de investigación (altmetrics, redcube, ect). También están a su disposición las secciones de Nature Events, Nature Jobs, Portales Regionales, calendario de conferencias y nuestra sección multimedia donde podrán consultar y descargar videos, blogs y podcast con información relevante del mundo de la ciencia. 
