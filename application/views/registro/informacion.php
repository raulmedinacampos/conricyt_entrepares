      <h2>Las área del conocimiento que cubre IOP.</h2>
<p>
El IOP (Institute of Physics) es una sociedad científica fundada en 1873, cuya misión es promover el avance y la difusión de la física a nivel mundial. Cuenta con una división de publicaciones, IOP Publishing, ofreciendo publicaciones para distribuir y dar visibilidad a la investigación científica de vanguardia a nivel mundial en todas las áreas de las ciencias básicas, desde medio ambiente, matemáticas, biología y química, hasta astronomía, ingeniería, medicina y todas las áreas que se relacionan con la física.
</p>

